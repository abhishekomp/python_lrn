# Creating a tuple using ()
t = (1, 2, 3, 4, 5)

# Printing a tuple
print(t)
print(f"Element at index 2 is: {t[2]}")
print(t.count(1))
print(
    f"The index of element 1 is: {t.index(1)}"
)  # index of the first occurence of an element

# Empty tuple
t1 = ()

# Tuple with 1 element
t2 = (1,)
print(t2)
